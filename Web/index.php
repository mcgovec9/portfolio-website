                <div class="projectInfo whichprep">
                    <b>What it is</b><br>
                    My Final Year Project, which received a 2.1 grade.<br><br>
                    <b>What it does</b><br>

                    WhichPrep is an educational application developed for Android.
                    It aims to aid the user, typically a non-native English speaker, in their use of prepositional words in sentences.
                    This is the main area in which people learning the English language have difficulty with.<br><br>

                    The app tests the user by presenting them with a series of sentences missing the prepositional word.
                    These sentences are scraped dynamically from articles on the web,
                    ensuring the user is never tested on the same question twice.
                    The user can either choose the correct word from a list of three choices,
                    or use the phones microphone to speak the word they feel is correct.<br><br>

                    The app analyzes user choice and time spent per question and through this provides informative feedback and identifies weaknesses
                    so as to improve the users use of the English language.<br><br>
                    <b>Technologies used</b><br>
                    Android Studio, Java, XML, JSoup, SQLite, ASR.
